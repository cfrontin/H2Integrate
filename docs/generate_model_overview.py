# ---------------------------------------------------------------------------
# Classification logic
# ---------------------------------------------------------------------------


def _classify_role(class_name: str, cls: type) -> str:
    """Determine which role bullet a model class should appear under.

    The classification looks at the MRO (so a model that inherits any of the
    base classes registered below is picked up automatically) plus a small
    set of explicit overrides for models that combine multiple roles. As a
    final fallback, a class whose name ends with ``FinanceModel`` or
    ``FinancialModel`` is classified as ``financial`` (used for technology
    finance wrappers that do not share a common base class).
    """
    if class_name in COMBINED_PERFORMANCE_AND_COST_MODELS:
        return "combined_performance_and_cost"
    if class_name in COMBINED_COST_AND_FINANCIAL_MODELS:
        return "combined_cost_and_financial"

    mro_names = {base.__name__ for base in cls.__mro__}

    if "CostModelBaseClass" in mro_names:
        return "cost"
    if "ProFastBase" in mro_names or cls.__module__.startswith("h2integrate.finances"):
        return "financial"
    if "PyomoRuleBaseClass" in mro_names or cls.__module__.startswith(
        "h2integrate.control.control_rules"
    ):
        return "dispatch_rule"
    if cls.__module__.startswith("h2integrate.control.control_strategies"):
        return "controller"
    if "PerformanceModelBaseClass" in mro_names:
        return "performance"
    if cls.__name__.endswith(("FinanceModel", "FinancialModel")):
        return "financial"
    return "other"


def _category_for_module(module_path: str) -> str | None:
    """Map a dotted module path to one of the top-level categories."""
    if not module_path.startswith("h2integrate."):
        return None
    # h2integrate.<top>.<...>
    parts = module_path.split(".")
    if len(parts) < 2:
        return None
    top = parts[1]
    return top if top in CATEGORIES else None


def _subcategory_for_module(module_path: str) -> tuple[str, str, str]:
    """Resolve the (subpackage key, label, description) for a class' module.

    Looks for the longest match in :data:`SUBCATEGORY_LABELS`. Falls back to
    the immediate subpackage name (with no description) if no entry matches.
    """
    parts = module_path.split(".")
    # Walk from the most specific subpackage down to the least specific.
    for depth in range(len(parts) - 1, 1, -1):
        candidate = ".".join(parts[:depth])
        if candidate in SUBCATEGORY_LABELS:
            label, desc = SUBCATEGORY_LABELS[candidate]
            return candidate, label, desc

    # Fallback: use the second-from-top segment (e.g. "wind" from
    # ``h2integrate.converters.wind.wind_pysam``).  No description.
    if len(parts) >= 3:
        return ".".join(parts[:3]), parts[2], ""
    return module_path, parts[-1], ""


