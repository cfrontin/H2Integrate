@dataclass
class ModelEntry:
    class_name: str  # The key under which the class is registered.
    actual_name: str  # ``cls.__name__`` -- may differ from class_name (e.g. ``cable``).
    module_path: str
    role: str
    subcat_key: str
    subcat_label: str
    subcat_desc: str
    summary: str  # First sentence of the class docstring; ``""`` if absent.


# Regex used to split off the first sentence of a docstring.  Matches a
# sentence terminator (``.``, ``!``, or ``?``) followed by whitespace.  We
# explicitly avoid splitting on ``e.g.``, ``i.e.``, ``etc.``, etc.
_SENTENCE_SPLIT_RE = re.compile(r"(?<!\b[A-Za-z])(?<=[.!?])\s+")

_ABBREVIATIONS = ("e.g.", "i.e.", "etc.", "vs.", "cf.", "Inc.", "Ltd.")


def _first_sentence_of_docstring(cls: type) -> str:
    """Return the first sentence of ``cls``'s own docstring, or ``""``.

    We deliberately ignore inherited docstrings here: a class without its own
    ``__doc__`` should render with no summary so the gap is visible (and so
    we don't surface unhelpful "Baseclass to be used for all cost models"
    strings for every uncommented subclass). The summary paragraph is
    collapsed onto a single line and truncated at the first sentence
    terminator that isn't part of a common abbreviation.
    """
    raw = cls.__dict__.get("__doc__")
    if not raw:
        return ""
    raw = inspect.cleandoc(raw)
    if not raw:
        return ""

    # Take only the leading paragraph (the conventional "summary line").
    paragraph = raw.split("\n\n", 1)[0].strip()
    if not paragraph:
        return ""
    # Collapse internal whitespace so the rendered bullet stays on one line.
    paragraph = " ".join(paragraph.split())

    # Walk forward and stop at the first sentence terminator that isn't part
    # of a known abbreviation.
    for match in re.finditer(r"[.!?]", paragraph):
        end = match.end()
        head = paragraph[:end]
        if any(head.endswith(abbr) for abbr in _ABBREVIATIONS):
            continue
        if end == len(paragraph) or paragraph[end].isspace():
            return head.strip()

    return paragraph


def collect_models() -> dict[str, dict[str, list[ModelEntry]]]:
    """Walk the registry and group classes by ``category -> subcategory_key``.

    Each subcategory bucket is a list of :class:`ModelEntry` instances, sorted
    by class name and then by role.
    """
    grouped: dict[str, dict[str, list[ModelEntry]]] = defaultdict(lambda: defaultdict(list))

    for class_name in sorted(supported_models.keys()):
        if class_name in HIDDEN_MODELS:
            continue
        try:
            cls = supported_models[class_name]
        except Exception as exc:  # noqa: BLE001 - lazy imports may raise anything; we warn and skip
            warnings.warn(
                f"Could not import model {class_name!r}: {exc}. "
                "It will be omitted from the auto-generated overview.",
                stacklevel=2,
            )
            continue

        module_path = cls.__module__
        category = _category_for_module(module_path)
        if category is None:
            # Skip anything we can't categorize (currently nothing).
            continue

        subcat_key, subcat_label, subcat_desc = _subcategory_for_module(module_path)
        role = _classify_role(class_name, cls)

        grouped[category][subcat_key].append(
            ModelEntry(
                class_name=class_name,
                actual_name=cls.__name__,
                module_path=module_path,
                role=role,
                subcat_key=subcat_key,
                subcat_label=subcat_label,
                subcat_desc=subcat_desc,
                summary=_first_sentence_of_docstring(cls),
            )
        )

    return grouped


# ---------------------------------------------------------------------------
# Markdown rendering
# ---------------------------------------------------------------------------


def _role_sort_key(entry: ModelEntry) -> tuple[int, str]:
    try:
        idx = ROLE_ORDER.index(entry.role)
    except ValueError:
        idx = len(ROLE_ORDER)
    return (idx, entry.class_name)


def _render_class_link(entry: ModelEntry) -> str:
    """Render a class as a cross-reference to its autodoc entry.

    Uses the MyST ``{py:class}`` role so the link resolves to the page that
    ``autosummary`` generates for the class. The leading ``~`` makes the
    rendered link text just the class name (no module prefix). If the class
    has a docstring, its first sentence is appended after a hyphen to give
    the bullet some context at a glance.

    When the registry key differs from the actual class name (e.g. the
    transport ``cable`` / ``pipe`` aliases), the registry key is shown
    alongside the class link so users can find both in the YAML and in code.
    """
    link = f"{{py:class}}`~{entry.module_path}.{entry.actual_name}`"
    if entry.class_name != entry.actual_name:
        link = f"{link} (registered as `{entry.class_name}`)"
    if entry.summary:
        return f"{link} - {entry.summary}"
    return link


def render_category(category: str, subcats: dict[str, list[ModelEntry]]) -> str:
    """Render the markdown for a single top-level category."""
    spec = CATEGORIES[category]
    lines: list[str] = []
    lines.append(f"({spec.anchor})=")
    lines.append(f"## {spec.heading}")
    lines.append("")

    # Sort subcategories by label so the rendered output is stable and
    # alphabetical within each category.
    def _subcat_sort_key(item: tuple[str, list[ModelEntry]]) -> str:
        entries = item[1]
        return entries[0].subcat_label if entries else item[0]

    for _subcat_key, entries in sorted(subcats.items(), key=_subcat_sort_key):
        if not entries:
            continue
        label = entries[0].subcat_label
        desc = entries[0].subcat_desc
        header = f"- `{label}`" + (f": {desc}" if desc else "")
        lines.append(header)

        # Group entries within this subcategory by role, preserving role order.
        by_role: dict[str, list[ModelEntry]] = defaultdict(list)
        for entry in entries:
            by_role[entry.role].append(entry)

        for role in ROLE_ORDER:
            role_entries = by_role.get(role)
            if not role_entries:
                continue
            lines.append(f"  - {ROLE_LABELS[role]}:")
            lines.extend(
                f"    + {_render_class_link(entry)}"
                for entry in sorted(role_entries, key=_role_sort_key)
            )
        lines.append("")

    return "\n".join(lines).rstrip() + "\n"


def render_overview(grouped: dict[str, dict[str, list[ModelEntry]]]) -> str:
    """Render the full auto-generated block (between marker comments)."""
    intro = (
        "<!--\n"
        "    THIS BLOCK IS AUTO-GENERATED by ``docs/generate_model_overview.py``.\n"
        "    Do not edit by hand. To add or rename a model, update the registry in\n"
        "    ``h2integrate/core/supported_models.py``. To change a subcategory\n"
        "    label or description, edit ``SUBCATEGORY_LABELS`` in the generator.\n"
        "-->\n\n"
        "(technology-models-overview)=\n"
        "# Technology Models Overview\n\n"
        "The tables below are generated at docs build time from\n"
        "[`supported_models.py`](../../h2integrate/core/supported_models.py)\n"
        "and the package source tree, so they always reflect the models that\n"
        "are actually registered with H2Integrate. Each class name links to its\n"
        "auto-generated API page.\n\n"
    )

    sections = []
    for category, _spec in sorted(CATEGORIES.items(), key=lambda kv: kv[1].order):
        subcats = grouped.get(category)
        if not subcats:
            continue
        sections.append(render_category(category, subcats))

    return intro + "\n".join(sections)


# ---------------------------------------------------------------------------
# File-level rewriting
# ---------------------------------------------------------------------------


def rewrite_overview(target: Path, generated_block: str) -> None:
    """Replace the block between the marker comments in ``target``.

    Raises ``RuntimeError`` if the markers are missing or out of order so that
    the docs build fails loudly rather than silently producing stale content.
    """
    original = target.read_text(encoding="utf-8")
    begin = original.find(BEGIN_MARKER)
    end = original.find(END_MARKER)
    if begin < 0 or end < 0 or end < begin:
        raise RuntimeError(
            f"Could not find marker pair '{BEGIN_MARKER}' .. '{END_MARKER}' "
            f"in {target}. Add the markers to delimit the auto-generated section."
        )

    before = original[: begin + len(BEGIN_MARKER)]
    after = original[end:]
    new_content = f"{before}\n{generated_block}\n{after}"
    if new_content != original:
        target.write_text(new_content, encoding="utf-8")


def main() -> int:
    target = Path(__file__).resolve().parent / "user_guide" / "model_overview.md"
    if not target.exists():
        print(f"ERROR: target file does not exist: {target}", file=sys.stderr)
        return 1

    grouped = collect_models()
    block = render_overview(grouped)
    rewrite_overview(target, block)
    n_models = sum(len(entries) for subs in grouped.values() for entries in subs.values())
    print(f"Updated {target.relative_to(target.parents[2])} with {n_models} models.")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
