"""Auto-generate the model registry section of ``docs/user_guide/model_overview.md``.

This script discovers every model that is registered in
:data:`h2integrate.core.supported_models.supported_models`, classifies each one
by category (resource, converter, transport, storage, control, etc.) and role
(performance, cost, financial, controller, ...), and rewrites the block
delimited by ``BEGIN_AUTOGENERATED`` / ``END_AUTOGENERATED`` markers in
``docs/user_guide/model_overview.md``.

The script is invoked from ``docs/build_book.sh`` before Jupyter Book runs so
that the docs are always in sync with the registry. It can also be run
manually:

.. code-block:: bash

    python docs/generate_model_overview.py

If a registered model fails to import, a warning is printed and the model is
listed without role information so the build still succeeds.
"""

